#
# Запуск:  python challenge_sessions_1_4.py
#
# Идея: в одном сценарии — кортеж (фиксированное меню дня), словарь (цены),
# список (что выбрал человек), циклы while / for, if, input, print.
#
# В эталоне ниже встречается continue — это НОВАЯ конструкция: сначала открой и запусти
#   session4/lesson4_continue_intro.py
# (там break vs continue и мини-пример). Без этого эталон «с нуля» нечестный — упущение исправлено.
#
# =============================================================================
# Сюжет простыми словами
# =============================================================================
#
# На сегодня меню зафиксировано — три блюда в кортеже (порядок и состав не меняем).
# Цены лежат в словаре: блюдо -> рубли.
# Человек по очереди вводит названия блюд; каждое удачное попадание кладём в список заказа.
# Пустая строка — конец заказа.
# Если ввёл то, чего нет в меню — вежливое сообщение, в заказ не кладём.
# В конце: если заказ пуст — одна фраза; иначе — построчно позиции с ценами и сумма.
#
# =============================================================================
# Пошагово (пиши сам; эталон внизу файла — для сверки, не копипаста с закрытыми глазами)
# =============================================================================
#
# 1) Создай кортеж из трёх строк — три блюда на сегодня, например: суп, каша, компот.
#
# 2) Создай словарь: те же три блюда как ключи, цены (целые числа) как значения.
#
# 3) Пустой список order — сюда будут попадать названия выбранных блюд.
#
# 4) Цикл while True:
#    - спроси input одной строкой: какое блюдо добавить (пустой Enter — закончить заказ).
#    - если строка пустая после strip() — break.
#    - иначе если такого блюда НЕТ среди меню (подсказка: проверка через кортеж меню
#      или через словарь цен — что тебе понятнее) — print «нет в меню» и снова цикл.
#    - иначе append блюда в order.
#
# 5) После цикла:
#    - если len(order) == 0 — print, что заказ пуст.
#    - иначе:
#        * total = 0
#        * for по order: взять цену из словаря по названию блюда, прибавить к total,
#          вывести строку вида: блюдо — цена (как хочешь: один print или f-строка).
#        * вывести итоговую сумму.
#
# =============================================================================
# Проверка руками
# =============================================================================
#
# - Сразу Enter: пустой заказ.
# - Два блюда из меню + Enter: две строки позиций + сумма верная по словарю.
# - Слово «пицца»: сообщение «нет в меню», в сумму не входит.
#
# =============================================================================
# ЭТАЛОН (сверка после своей попытки)
# =============================================================================

MENU = ("суп", "каша", "компот")
prices = {"суп": 120, "каша": 80, "компот": 50}

order = []

while True:
    dish = input("Блюдо в заказ (Enter — готово): ").strip()
    if dish == "":
        break
    if dish not in MENU:
        print("Такого нет в меню на сегодня.")
        continue  # следующий ввод, без append (см. lesson4_continue_intro.py)
    order.append(dish)

if len(order) == 0:
    print("Заказ пуст.")
else:
    total = 0
    print("--- Ваш заказ ---")
    for item in order:
        cost = prices[item]
        total = total + cost
        print(item, "—", cost, "руб.")
    print("Итого:", total, "руб.")
