#
# Про запятую после ОДНОГО числа — почему в коде выше этого нет:
#
# В файле кортеж из ТРЁХ элементов: ("хлеб", "молоко", "сыр").
# Между элементами уже стоят запятые — Python понимает: это кортеж из трёх частей.
#
# Особый случай — кортеж из ОДНОГО числа. Напиши в голове:
#   (5)   — это НЕ кортеж, а просто число 5 в скобках (как в математике).
#   (5,)  — вот это кортеж из одного элемента. Запятая говорит Python: "это последовательность, не выражение".
# Поэтому правило "запятая обязательна" относится к кортежу из одного значения. В тройке элементов
# запятые уже есть между ними — отдельная "(5,)" в этом файле не нужна для демонстрации.
#
# Чем кортеж отличается от "просто переменных":
#
# Вариант А — три отдельные переменные:
#   a = "хлеб"
#   b = "молоко"
#   c = "сыр"
# Три разных имени. Передавать "всё сразу" в функцию или в цикл неудобно — это три куска.
#
# Вариант Б — одна переменная t, внутри неё три значения по порядку:
#   t = ("хлеб", "молоко", "сыр")
# Одно имя, одно целое. Можно передать t целиком, пройти for по t, взять t[0], t[1].
# То есть кортеж — это не "магия", а способ держать НЕСКОЛЬКО значений в ОДНОЙ переменной,
# с номерами 0, 1, 2… как в списке, только без замены элементов "по месту" как в списке.
#
# Коротко: переменная = одно значение. Кортеж (и список) = одно имя, много значений по порядку.
