# Сессия 4 — кортеж (tuple)
#
# Кортеж в круглых скобках: (1, 2, 3). Как список, но "жёсткий" — не делаем t[0] = ...
# Индекс с нуля: первый t[0], второй t[1].
# Один элемент: (5,) — запятая обязательна, иначе (5) это просто число.

t = ("хлеб", "молоко", "сыр")  # кортеж из трёх строк

print(t)        # весь кортеж
print(t[1])       # второй для человека = индекс 1 в Python

for x in t:       # каждый элемент на своей строке
    print(x)

