# Сессия 4 — continue и break (должно быть ДО челленджа 1_4, если в эталоне есть continue)
#
# break  — выйти из цикла полностью.
# continue — не выходить из цикла, а пропустить ОСТАТОК этой итерации и начать следующую.
#
# Запуск: python session4/lesson4_continue_intro.py

# Пример: печатаем только чётные числа 0..4 (не печатаем нечётные — continue)
print("--- только чётные ---")
for n in range(5):
    if n % 2 == 1:
        continue  # нечётное — сразу следующий n, print ниже не выполняется
    print(n)

# То же без continue — через else-ветку у if (логика та же, другой стиль)
print("--- то же словами if/else ---")
for n in range(5):
    if n % 2 == 0:
        print(n)
