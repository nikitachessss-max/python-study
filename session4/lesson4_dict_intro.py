# Сессия 4 — словарь (dict): первое знакомство
#
# --- ОБЪЁМ ТЕМЫ (чтобы не было сюрпризов) ---
#
# Словарь — это набор пар «ключ → значение». Как табличка: в левой колонке подпись,
# в правой — что лежит под этой подписью. По ключу быстро находишь значение.
#
# Сейчас в этом файле: как создать словарь, взять значение по ключу, заменить значение,
# добавить новую пару.
#
# Дальше по файлам: session4/lesson4_dict_for.py (for по словарю), lesson4_tuple_intro.py.
# Позже (не сегодня): вложенные словари, метод .get() — подключим по мере надобности.
#
# --- ПРОСТЫМИ СЛОВАМИ ---
#
# Словарь пишут в фигурных скобках. Ключ и значение разделяют двоеточием, пары — запятыми:
#   { "яблоко": 50, "груша": 70 }
#
# Взять значение: имя_словаря["ключ"]  — в квадратных скобках ключ, как индекс у списка,
# только это не номер позиции, а подпись (часто строка).
#
# Изменить значение:  имя_словаря["ключ"] = новое_число
# Добавить пару:       имя_словаря["новый_ключ"] = значение
#
# Мини-пример (образец, не обязательно копировать дословно):
#   prices = { "чай": 100, "кофе": 150 }
#   print(prices["чай"])     # 100
#   prices["чай"] = 120      # цена изменилась
#   prices["сок"] = 90       # новая позиция
#
# --- ЗАДАЧА В ЭТОМ ФАЙЛЕ ---
#
# 1) Создай словарь с двумя любыми товарами (строка) и ценами в рублях (целые числа).
# 2) Выведи цену одного из товаров через print( ... ["..."] ).
# 3) Поменяй цену одного товара (одна строка присваивания).
# 4) Добавь третий товар с ценой.
# 5) Выведи весь словарь одним print — увидишь все пары.
#
# Проверка: сначала одна цена, потом после изменений print показывает три товара.
#
# --- ЗАПУСК ---
#
#   python session4/lesson4_dict_intro.py
#
# Пиши ниже.
product = { "сахар": 100, "соль": 200 }
print(product["сахар"])
product["сахар"] = 120
product["сок"] = 150
print(product) 