# Просто запусти и посмотри, что напечатается.

shop = {"хлеб": 40, "молоко": 80, "сыр": 120}

# --- БЛОК 1: что попадает в item? ---
print("--- что в item ---")
for item in shop:
    print(item)

# --- БЛОК 2: а что тогда shop[item]? ---
print("--- item и shop[item] ---")
for item in shop:
    print(item, "стоит", shop[item])

