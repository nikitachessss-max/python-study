# Ввод фильмов в цикле; стоп — пустая строка (Enter).
# Если ничего не ввели — «Ничего не ввёл.»; иначе — нумерованный список 1. … 2. …
# Проверка: сразу Enter → одна фраза; несколько фильмов + Enter → строки с номерами.

films = []  # сюда по одному названию

while True:  # пока сам не остановишься
    name = input("Фильм (пустой Enter — стоп): ")
    if name == "":  # сигнал «хватит»
        break
    films.append(name)  # дописать в конец списка

if len(films) == 0:  # список пуст
    print("Ничего не ввёл.")
else:
    for i in range(len(films)):  # i = 0, 1, … — индекс каждой позиции
        print(str(i + 1) + ". " + films[i])  # номер для человека + одно имя films[i]

