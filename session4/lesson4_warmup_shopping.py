#
# Пользователь вводит товары для списка покупок — по одному в строке.
# Стоп: пустая строка (только Enter).
#
# Если ничего не ввели — напечатать что-то вроде: Список пуст.
# Если ввели — вывести нумерованный список: 1. хлеб, 2. молоко, …
#
# Проверка: сразу Enter → одна фраза про пустой список.
# Несколько товаров + Enter → строки с номерами, без печати всего списка целиком на каждой строке.
#
# Подсказки по коду (тот же каркас, что в разминке с фильмами):
#   список = []  # своё имя переменной, например items
#   while True → input → если пусто → break → иначе append
#   потом if len(...) == 0 → print ... else → for i in range(len(...)) → print номер и items[i]
#
# Пиши код ниже.

items = []  # имя list лучше не использовать — это встроенное слово в Python
while True:
    product = input("название товара (Enter — стоп): ")
    if product == "":
        break  # было не под if: в Python тело if обязано быть с отступом
    items.append(product)

if len(items) == 0:
    print("Список пуст.")
else:
    number = 1
    for i in range(len(items)):
        print(number, items[i], sep=". ")  # было film[i] — переменной film нет; нужен items[i]
        number = number + 1  # тело for тоже должно быть с отступом внутри for