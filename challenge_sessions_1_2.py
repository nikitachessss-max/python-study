# Integrated challenge: sessions 1 + 2 (Магазин за смену)
#
# Запуск: python challenge_sessions_1_2.py
#
# Ты — кассир. Нужно принять несколько покупок, посчитать выручку и скидки,
# потом закрыть смену секретным словом.
#
# --- Часть A (сессия 1: переменные, input, print, if/elif/else, f-строки) ---
#
# 1. Спроси имя кассира (текст, input без int).
# 2. Выведи приветствие: f"Смена открыта, {имя}!"
#
# --- Часть B (сессия 2: for, range, аккумулятор, if внутри цикла) ---
#
# 3. Спроси целое число: сколько покупок за смену (например n).
# 4. Создай total = 0.0 (общая выручка со скидкой).
# 5. Создай count_discount = 0 (сколько раз дали скидку больше 0%).
# 6. Цикл: for i in range(1, n + 1):  # покупка номер 1, 2, ... n
#    Внутри цикла:
#    6a. Спроси цену товара (float).
#    6b. Спроси возраст покупателя (int).
#    6c. Скидка по возрасту (как в калькуляторе скидок / билете в кино):
#         до 18 лет — 10% (0.1)
#         от 18 до 64 — 5% (0.05)
#         65 и старше — 20% (0.2)
#    6d. Итог по этой покупке: price * (1 - discount)  (или "лобовой": price - price * discount)
#    6e. Прибавь итог к total.
#    6f. Если discount > 0: увеличь count_discount на 1.
#    6g. Выведи одной строкой f-строкой: номер покупки, итог по ней (например:
#         f"Покупка {i}: к оплате {итог} руб.")
#
# 7. После цикла выведи двумя print (или одним блоком):
#    - общую выручку за смену (total)
#    - сколько покупок было со скидкой (count_discount)
#
# --- Часть C (сессия 2: while, сравнение строк) ---
#
# 8. Напиши: "Для закрытия смены введите слово: закрыто"
# 9. Спроси слово через input().strip() — сохрани в переменную word.
# 10. Пока word не равно "закрыто" (без кавычек в условии — само слово в кавычках в коде):
#     - выведи "Неверно, попробуйте снова."
#     - снова спроси слово с .strip()
# 11. После цикла выведи: f"Смена закрыта, {имя кассира}. До завтра!"
#
# Подсказки:
# - Отступы: тело for — 4 пробела; тело if внутри for — 8 пробелов.
# - Не путай print и присваивание.
# - Переменная с именем кассира нужна в шагах 2 и 11 — создай её в шаге 1 и не перезаписывай.
#
# Пиши код ниже:

name = input("Как вас зовут? ")
print(f"Смена открыта, {name}!")

n = int(input("Сколько покупок за смену? "))
total = 0.0
count_discount = 0

for i in range(1, n + 1):
    price = float(input(f"Покупка {i}: цена товара? "))
    age = int(input("Возраст покупателя? "))
    if age < 18:
        discount = 0.1
    elif age <= 64:
        discount = 0.05
    else:
        discount = 0.2
    pay = price * (1 - discount)
    total = total + pay
    if discount > 0:
        count_discount = count_discount + 1
    print(f"Покупка {i}: к оплате {pay} руб.")

print(f"Выручка за смену: {total} руб.")
print(f"Покупок со скидкой (не 0%): {count_discount}")

print("Для закрытия смены введите слово: закрыто")
word = input("Введите слово: ").strip()
while word != "закрыто":
    print("Неверно, попробуйте снова.")
    word = input("Введите слово: ").strip()
print(f"Смена закрыта, {name}. До завтра!")

