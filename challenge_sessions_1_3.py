name = input("Как тебя зовут? ")
print(f"Смена открыта, {name}! Сегодня печатаем чек с позициями.")

n = int(input("Сколько позиций в чеке? "))
lines = []
total = 0.0

for i in range(1, n + 1):
    product = input("Введи название товара: ")
    cost = float(input("Введи цену: "))
    total = total + cost
    line = f"{i}. {product} — {cost} руб."
    lines.append(line)

print(f"Итого по чеку: {total} руб.")
print(f"В чеке позиций: {len(lines)}")

if len(lines) >= 3:
    print("Первые три строки чека (срез):")
    print(lines[:3])

print("--- Чек ---")
for row in lines:
    print(row)

print('Для завершения печати введите слово: готово')
word = input("Введите слово: ").strip()
while word != "готово":
    print("Неверно. Введите ровно слово: готово")
    word = input("Введите слово: ").strip()

print(f"Печать завершена, {name}. Хорошей смены!")

