# Дополнительная практика на while
#
# Задание 1: "Обратный отсчёт"
#   - Создай переменную count = 10
#   - Пока count больше 0 — выводи count и уменьшай на 1
#   - Когда дойдёт до 0 — выведи "Поехали!"
#   Подсказка: уменьшить на 1 — это count = count - 1
#
# Задание 2: "Калькулятор"
#   - Спрашивай у пользователя числа по одному
#   - Прибавляй каждое к сумме (как в копилке)
#   - Если пользователь вводит 0 — останавливайся
#   - Выведи итоговую сумму
#   Подсказка: условие while — "пока число НЕ РАВНО 0"
#
# Пиши код ниже
count = 10
while count > 0:
    print(count)
    count = count - 1
print("поехали")

total = 0
number = int(input("введи число (0 — стоп): "))
while number != 0:
    total = total + number
    number = int(input("введи число (0 — стоп): "))
print(f"итого: {total}")
