# Финальное задание сессии 2
# Программа "Проверка паролей"
#
# У тебя есть 3 пользователя. Для каждого нужно спросить пароль.
# Если пароль неправильный — спрашивать снова, пока не введёт правильный.
#
# Пошагово:
#
# Строка 1: создай переменную correct_password = "abc123" (правильный пароль для всех)
#
# Строка 2: напиши for — цикл на 3 повторения: for user in range(1, 4):
#   Это значит: user будет 1, потом 2, потом 3 (три пользователя)
#
# Строка 3 (с отступом): выведи f"Пользователь {user}, введите пароль:"
#
# Строка 4 (с отступом): спроси пароль через input() и сохрани в переменную password
#
# Строка 5 (с отступом): напиши while — пока password не равен correct_password:
#
# Строка 6 (двойной отступ — 8 пробелов): выведи "Неверный пароль! Попробуйте ещё:"
#
# Строка 7 (двойной отступ — 8 пробелов): спроси пароль ещё раз (password = input(...))
#
# Строка 8 (с отступом — 4 пробела, внутри for но вне while): выведи f"Пользователь {user}, вход выполнен!"
#
# Пиши код ниже
correct_password = "abc123"
for user in range(1, 4):
    print(f"Пользователь {user}, введите пароль:")
    password = input("пароль: ")
    while password != correct_password:
        print("неверный пароль! попробуйте ещё:")
        password = input("пароль: ")
    print(f"Пользователь {user}, вход выполнен!")
