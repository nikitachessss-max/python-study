# Задание: "Меню ресторана"
#
# Программа показывает меню и принимает заказы.
# Пользователь вводит цены блюд по одному.
# Когда вводит 0 — заказ завершён.
# В конце: вывести сумму заказа и количество блюд.
#
# Пошагово:
#
# Строка 1: total = 0 (сумма заказа, в начале ноль)
# Строка 2: count = 0 (сколько блюд заказано, в начале ноль)
# Строка 3: выведи "Введите цену блюда (0 — завершить заказ):"
# Строка 4: price = int(input("цена: "))
# Строка 5: while price != 0:
# Строка 6 (4 пробела): total = total + price
# Строка 7 (4 пробела): count = count + 1
# Строка 8 (4 пробела): price = int(input("цена: "))
# Строка 9 (без отступа): print(f"Заказ: {count} блюд, итого {total} руб.")
#
# Пиши код ниже
total = 0
count = 0
print ("введите цену блюда(0 - завершить заказ)")
price = int(input("цена"))
while price !=0:
      total = total + price
      count = count + 1
      price = int(input("цена:"))
print(f"заказ: {count} блюд, итого {total} руб")

