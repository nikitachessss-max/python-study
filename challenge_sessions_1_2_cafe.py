name = input("Как тебя зовут? ")
print(f"Смена открыта, {name}! Кофейня работает.")

n = int(input("Сколько заказов за смену? "))
total = 0.0
count_discount = 0

for i in range(1, n + 1):
    price = float(input(f"Заказ {i}: цена напитка? "))
    sale = input("Есть студенческий? (да/нет) ").strip().lower()
    if sale == "да":
        discount = 0.1
    else:
        discount = 0.0
    pay = price * (1 - discount)
    total = total + pay
    if discount > 0:
        count_discount = count_discount + 1
    print(f"Заказ {i}: к оплате {pay} руб.")

print(f"Выручка за смену: {total} руб.")
print(f"Заказов со скидкой: {count_discount}")

print("Для закрытия смены введите слово: конец")
word = input("Введите слово: ").strip()
while word != "конец":
    print("Неверно, попробуйте снова.")
    word = input("Введите слово: ").strip()
print(f"Смена закрыта, {name}. До завтра!")
