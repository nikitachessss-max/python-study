# У тебя есть список — это как коробка, куда складывают вещи по порядку.
# Сначала коробка может быть ПУСТАЯ. Потом ты кладёшь туда первую вещь, вторую,
# третью — в конец очереди.
#
# =============================================================================
# Пустая коробка в коде
# =============================================================================
#
# Пустой список пишут так: квадратные скобки, внутри ничего.
#
#   things = []
#
# Слово things — просто имя переменной, можешь назвать иначе (names, list1…).
#
# =============================================================================
# Как положить одну вещь в конец списка
# =============================================================================
#
# Пишешь ИМЯ_СПИСКА, точку, слово append, в скобках то, что кладёшь:
#
#   basket.append("молоко")
#
# Точка значит: «эта команда относится к этому списку». Слово append — встроенное
# в Python: «допиши в конец списка одно значение».
#
# Список при этом НЕ копируют в новую переменную — тот же самый список внутри
# переменной становится длиннее. Как в ту же коробку положили ещё один пакет.
#
# Пример по шагам:
#   basket = []              коробка пустая
#   basket.append("хлеб")    в коробке одна вещь
#   basket.append("сыр")     в коробке две вещи, сыр последний
#   Теперь basket это ["хлеб", "сыр"]
#
# =============================================================================
# Зачем тут цикл for
# =============================================================================
#
# Один раз append — руками написал. Нужно три раза спросить у человека слово и три
# раза положить в список — проще сделать цикл на три поворота (for с range(3)).
# На каждом повороте: input, потом append с тем, что ввели.
#
# =============================================================================
# 4) Твоя задача
# =============================================================================
#
# 1) Создай пустой список (одна переменная, = []).
#
# 2) Сделай цикл for с range(3) — то есть тело цикла выполнится три раза.
#
# 3) Внутри цикла: спроси у пользователя одну строку через input (можно короткий
#    текст приглашения в скобках input).
#
# 4) Добавь введённую строку в конец списка через .append(...).
#
# 5) После цикла (без отступа, на одном уровне с for) выведи весь список одним print.
#
# Проверка: запускаешь программу, три раза вводишь что-то с клавиатуры — в конце
# print показывает список из трёх строк в том порядке, как вводил.
#
# =============================================================================
# Запуск:  python session3/lesson3_list_append.py
# =============================================================================
#
# --- КОД ---
city = []
for s in range(3):
    line = input("Введи город: ")
    city.append(line)
print(city)