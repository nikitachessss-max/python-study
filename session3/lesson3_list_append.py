# =============================================================================
# 1) Пустой список
# =============================================================================
#
# Квадратные скобки без элементов внутри — пустой список, в который потом можно
# что-то добавлять:
#
#   things = []
#
# =============================================================================
# 2) Что делает .append(...)
# =============================================================================
#
# Это метод списка (точка после имени списка). В скобках — одно значение, которое
# дописывается В КОНЕЦ списка. Сам список меняется «на месте», новую переменную
# создавать не нужно.
#
# Пример:
#   basket = []
#   basket.append("яблоко")
#   basket.append("груша")
#   # теперь basket == ["яблоко", "груша"]
#
# =============================================================================
# 3) Зачем цикл
# =============================================================================
#
# Один и тот же приём «спросить — append» удобно повторить несколько раз через
# for ... in range(сколько_раз):
#
# =============================================================================
# 4) Твоя задача
# =============================================================================
#
# 1) Создай пустой список (одна переменная, = []).
#
# 2) Сделай цикл for с range(3) — то есть тело цикла выполнится три раза.
#
# 3) Внутри цикла: спроси у пользователя одну строку через input (можно короткий
#    текст приглашения в скобках input).
#
# 4) Добавь введённую строку в конец списка через .append(...).
#
# 5) После цикла (без отступа, на одном уровне с for) выведи весь список одним print.
#
# Проверка: запускаешь программу, три раза вводишь что-то с клавиатуры — в конце
# print показывает список из трёх строк в том порядке, как вводил.
#
# =============================================================================
# Запуск:  python session3/lesson3_list_append.py
# =============================================================================
