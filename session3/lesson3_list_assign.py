# После таких print список в памяти тот же самый. Ты ничего не перезаписал.
#
# Здесь другое действие: ПЕРЕЗАПИСЬ одной позиции. Нужен знак присваивания =,
# но слева не просто имя переменной, а «ячейка» списка с индексом:
#
#   название_списка[номер_позиции] = новое_значение
#
# Смысл: «возьми список, найди позицию с этим номером, выкинь оттуда старое
# и положи новое». Остальные элементы на своих местах.
#
# Аналогия: в intro ты только прочитал строку в таблице. Здесь ты стёр то, что
# было в одной клетке, и вписал другое.
#
# --- ОДНА СТРОКА, КОТОРОЙ НЕ БЫЛО В INTRO ---
#
# Именно конструкция вида:  что-то[индекс] = значение
# (равно между квадратными скобками и новым значением — главный новый навык.)
#
# Пример другой тематики, чем во intro (чтобы не казалось «то же самое»):
#   дни = ["пн", "вт", "ср"]
#   # было три будних дня
#   дни[1] = "выходной"
#   # стало: первая позиция "пн", вторая уже не "вт", а "выходной", третья "ср"
#   # то есть список после этой строки уже ДРУГОЙ — внутри него заменили один элемент
#
# Обрати внимание: в примере нет второго print в комментарии — твоя задача самому
# увидеть разницу «до» и «после» через два print в программе.
#
# --- ЗАДАЧА ---
#
# 1) Создай свой список из трёх строк (любые три значения).
#
# 2) Выведи список целиком один раз — это снимок «до замены».
#
# 3) Одной строкой кода замени элемент с индексом 2 (третья позиция для человека)
#    на другую строку — не ту, что там стояла.
#
# 4) Выведи тот же список целиком второй раз — в выводе должны совпасть первые
#    два элемента с первым print, а третий — уже новый.
#
# --- ЗАПУСК ---
#
#   python session3/lesson3_list_assign.py
#
# --- КОД ---
