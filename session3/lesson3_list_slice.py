# Сессия 3 — список: срезы [начало:конец]
#
# --- ЧТО ЭТО (и чем не то же самое, что один индекс) ---
#
# Один индекс — один элемент:  words[2]  → одна строка (или число).
#
# Срез — кусок списка, тоже список (может быть коротким или пустым):
#
#   имя[начало:конец]
#
# Важнейшее правило Python: позиция справа от двоеточия **не входит** в результат.
# Говорят: «конец — исключая», «до конца не дотягиваем».
#
# Пример на цифрах (позиции сверху, как в голове):
#   nums = [10, 20, 30, 40, 50]
#   индексы:  0   1   2   3   4
#
#   nums[1:4]  →  [20, 30, 40]
#   то есть с индекса 1 включительно до индекса 4 **не включая** — берём 1,2,3.
#
# Если опустить начало — с самого старта:  nums[:3]  → первые три элемента.
# Если опустить конец — до самого конца:  nums[2:]  → с индекса 2 и до конца.
#
# --- ЗАЧЕМ В ОЧЕРЕДИ ТЕМ ---
#
# Срезы — всё ещё «квадратные скобки и позиции», без циклов. После этого файла
# дальше по плану сессии 3: перебор for по списку → .append() → удаление (del / pop).
#
# --- ЗАДАЧА ---
#
# 1) Создай список из пяти строк (любой тематики).
#
# 2) Выведи срез: с индекса 1 включительно до индекса 4 не включительно — то есть
#    в квадратных скобках два числа через двоеточие (подставь свои 1 и 4 по смыслу
#    задания: именно [1:4]).
#
# 3) Выведи второй срез: от начала списка до индекса 3 не включительно — используй
#    сокращённую запись с пустым началом:  [:3].
#
# Проверка: первый print — ровно три элемента (позиции 1,2,3 в исходном списке).
# Второй print — три элемента (позиции 0,1,2).
#
# --- ЗАПУСК ---
#
#   python session3/lesson3_list_slice.py
#
# --- КОД ---
word = ["залупа", "хер", "давалка", "хуй", "блядина"]
print(word[1:4])
print(word[:3])
