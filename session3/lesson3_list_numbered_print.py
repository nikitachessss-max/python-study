items = ["хлеб", "молоко", "сыр", "яйца"]


# =============================================================================
# Вариант 1 — range(len(...)) и индекс
# =============================================================================
# Индекс в Python с нуля, поэтому номер для человека: i + 1

def variant_1_range_len(items):
    print("--- Вариант 1: range(len(items)), номер i + 1 ---")
    for i in range(len(items)):
        print(i + 1, items[i])


# =============================================================================
# Вариант 2 — то же, но красивее через f-строку
# =============================================================================

def variant_2_fstring(items):
    print("--- Вариант 2: f-строка с точкой ---")
    for i in range(len(items)):
        print(f"{i + 1}. {items[i]}")


# =============================================================================
# Вариант 3 — номер от 1 до len: range(1, len+1) и индекс i - 1
# =============================================================================
# В переменной i сразу 1, 2, 3… как на чеке; в список лезем через i - 1

def variant_3_range_one_based(items):
    print("--- Вариант 3: for i in range(1, len(items) + 1) ---")
    for i in range(1, len(items) + 1):
        print(i, items[i - 1])


# =============================================================================
# Вариант 4 — for по элементам + счётчик руками
# =============================================================================
# Полезно понимать логику «сначала 0, в цикле увеличиваю на 1»

def variant_4_manual_counter(items):
    print("--- Вариант 4: счётчик num = 0, в цикле num = num + 1 ---")
    num = 0
    for x in items:
        num = num + 1
        print(num, x)


# =============================================================================
# Вариант 5 — enumerate(..., start=1)
# =============================================================================
# Встроенная функция: отдаёт пары (номер, элемент). start=1 — нумерация с единицы.
# Удобно и часто в реальном коде; смысл тот же, что варианты выше.

def variant_5_enumerate(items):
    print("--- Вариант 5: for i, x in enumerate(items, start=1) ---")
    for i, x in enumerate(items, start=1):
        print(i, x)


if __name__ == "__main__":
    variant_1_range_len(items)
