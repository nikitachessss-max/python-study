# Условие своими словами
# =============================================================================
#
# Ты ведёшь маленький список покупок: сначала коробка пустая, потом три раза
# спрашиваешь у пользователя название товара и кладёшь строку в конец списка.
# В конце один раз выводишь весь список — чтобы увидеть три позиции в порядке ввода.
#
# =============================================================================
# Что должно произойти при запуске
# =============================================================================
#
# Запускаешь скрипт → три раза появляется приглашение ввести товар → после третьего
# ввода print показывает список из трёх строк, например ['хлеб', 'молоко', 'яйца'].
#
# =============================================================================
# На что опереться
# =============================================================================
#
# - Пустой список: одна переменная = []
# - Цикл: for ... in range(3):  (три повтора)
# - Внутри: переменная = input("...")
# - Сразу после input: имя_списка.append(переменная_с_вводом)
# - После цикла (без лишнего отступа): print(имя_списка)
#
# =============================================================================
# Типичные ошибки новичка
# =============================================================================
#
# - Забыл append — ввёл в переменную, но список пустой в print
# - print внутри цикла — увидишь список три раза подряд, а не один итог
# - print с тем же отступом, что и append внутри for — тоже три раза
#
# =============================================================================
# Запуск:  python session3/lesson3_append_practice.py
# =============================================================================
#
# --- КОД ---
purchases = []
for s in range(3):
    line = input("введи товар:")
    purchases.append(line)
print(purchases)
