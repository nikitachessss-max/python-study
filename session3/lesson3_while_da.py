# -*- coding: utf-8 -*-
"""
Разминка сессии 3 — цикл while (после перерыва в учёбе)

ЗАЧЕМ ЭТА ЗАДАЧА
----------------
Ты уже делал похожую логику в challenge_sessions_1_2.py: закрытие смены секретным
словом — снова спрашивать ввод, пока не совпало. Здесь то же семейство задач,
но проще: одно условие, одна цель — чтобы руки вспомнили while и работу со строкой.

ЧТО НУЖНО СДЕЛАТЬ (УСЛОВИЕ)
---------------------------
Напиши программу, которая снова и снова просит ввести текст с клавиатуры, пока
пользователь не введёт ровно слово «да» (маленькими буквами, как в этой строке).

Пока введённое слово НЕ равно «да»:
  • нужно вывести короткое сообщение, что ответ неверный и нужно попробовать снова;
  • затем снова запросить ввод.

Когда наконец ввели именно «да» — выйти из цикла и один раз напечатать что-то
вроде «Отлично» или «Принято» (фразу выбери сам).

КАК ДОЛЖНО ВЫГЛЯДЕТЬ ПОВЕДЕНИЕ
------------------------------
• Если пользователь сразу ввёл «да» — лишних «неверно» быть не должно, только
  финальное подтверждение.
• Если вводил что угодно другое несколько раз — после каждого неверного раза
  сообщение и новый запрос.
• Строка «Да» с большой буквы в этой задаче НЕ считается верным ответом (так
  проще проверять условие); если захочешь принимать и большую букву — это уже
  отдельное услложнение, сейчас не обязательно.

НА ЧТО ОПЕРЕТЬСЯ (ТЕМЫ)
------------------------
• input() — спросить строку у пользователя.
• Сохранить ответ в переменную (присваивание), не только print — иначе программа
  не запомнит, что ввели.
• .strip() у строки после input — убрать пробелы в начале и конце; тогда ввод
  «   да   » всё равно можно считать «да», если хочешь так сделать (удобно).
• while и сравнение строк: пока переменная не равна нужной строке — повторять тело.
• Внутри цикла обязательно снова запросить input и снова сохранить в ту же
  переменную — иначе условие никогда не изменится и цикл зависнет навсегда.

ПОДВОДНЫЕ КАМНИ
---------------
• Путаница print(...) и variable = input(...) — спросить и запомнить это
  присваивание в переменную.
• Один раз спросили до цикла, потом в цикле — логика «первый ввод / пока не то /
  спросить ещё раз» должна быть согласована, чтобы не дублировать лишнего и не
  забыть обновить переменную внутри while.

КОД
---
Пиши код ниже этих комментариев. Готового решения здесь нет — это нормально;
если застрянешь надолго, напиши в чат — тогда подстроим подсказки или разбор.
"""

word = input("введи слово: ").strip()
while word != "да":
    print("неверно")
    word = input("введи слово еще раз: ").strip()
print("принято")