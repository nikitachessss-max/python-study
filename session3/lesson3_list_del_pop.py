# =============================================================================
# Что сейчас учим, а что потом
# =============================================================================
#
# Сейчас только одно: как УБРАТЬ элемент из списка.
#
# Два способа:
# 1) del список[индекс]
# 2) список.pop()
#
# Позже будет .pop(индекс) и .remove(значение), но не сейчас.
#
# =============================================================================
# Представь список как коробку
# =============================================================================
#
# Было:
#   food = ["хлеб", "сыр", "молоко", "чай"]
#
# Индексы:
#   "хлеб"   -> [0]
#   "сыр"    -> [1]
#   "молоко" -> [2]
#   "чай"    -> [3]
#
# =============================================================================
# del — просто выкинул и забыл
# =============================================================================
#
# Пишешь:
#
#   del food[1]
#
# Что это значит:
# - возьми список food
# - найди элемент с номером 1
# - убери его
#
# Было:
#   ["хлеб", "сыр", "молоко", "чай"]
#
# Стало:
#   ["хлеб", "молоко", "чай"]
#
# "сыр" исчез.
# Всё, что было справа, сдвинулось влево.
# Длина списка стала меньше на 1.
#
# Главное:
# del просто убирает.
# Он ничего тебе обратно не отдаёт.
#
# =============================================================================
# pop() — тоже убирает, но ещё даёт то, что вынул
# =============================================================================
#
# Пишешь:
#
#   x = food.pop()
#
# Что это значит:
# - возьми список food
# - сними ПОСЛЕДНИЙ элемент
# - положи его в переменную x
#
# Было:
#   ["хлеб", "сыр", "молоко", "чай"]
#
# После x = food.pop():
#   food -> ["хлеб", "сыр", "молоко"]
#   x    -> "чай"
#
# То есть pop делает две вещи сразу:
# 1) убирает элемент из списка
# 2) отдаёт этот элемент тебе в переменную
#
# Если переменная не нужна, можно и так:
#
#   food.pop()
#
# Тогда элемент просто пропадёт.
#
# =============================================================================
# Короткая разница в одной фразе
# =============================================================================
#
# del — "убери и выбрось".
# pop — "убери и дай мне в руку то, что убрал".
#
# =============================================================================
# ЗАДАЧА
# =============================================================================
#
# 1) Создай список из ЧЕТЫРЁХ строк (любой смысл: города, еда, фильмы — неважно).
#
# 2) Выведи список целиком print — это «до».
#
# 3) Удали элемент с индексом 1 через del имя[1].
#
# 4) Снова выведи список print — должно остаться три элемента.
#
# 5) Вызови .pop() без аргумента (с последнего элемента). Сохрани результат в
#    переменную, например removed = ... .pop()
#
# 6) Выведи print(removed) — что именно вынули.
#
# 7) Выведи print списка — осталось два элемента.
#
# Проверка: сначала 4 строки в списке, после del — 3, после pop — 2; отдельной
# строкой видно значение, которое снял pop.
#
# --- ЗАПУСК ---
#
#   python session3/lesson3_list_del_pop.py
#
# --- КОД ---
