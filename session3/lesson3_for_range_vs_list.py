# --- КОД (пример решения — смотри, потом попробуй написать сам с нуля) ---

# Задача A: for + range — переменная i по очереди 0, 1, 2, 3, 4
for i in range(5):
    print(i)

# Задача B: for + список строк — в переменной слово, печатаем с подписью
cities = ["омск", "томск", "тюмень"]
for word in cities:
    print(f"Город: {word}")

# Задача C: for + список чисел — те же 7,8,9 превращаются в 70,80,90 (не как счётчик 0,1,2)
numbers = [7, 8, 9]
for n in numbers:
    print(n * 10)

