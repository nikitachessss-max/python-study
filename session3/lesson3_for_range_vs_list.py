# Задача A
# -------
# Напиши цикл for по range(5). Внутри один print переменной цикла.
# Ожидание: пять строк — числа 0 1 2 3 4.
#
# Задача B
# -------
# Второй (отдельный) цикл: for по списку из трёх любых строк. Внутри print с f-строкой,
# чтобы было видно текст:  print(f"Город: {переменная}")
# Ожидание: три строки вида «Город: омск» — слова из списка, это не 0 1 2.
#
# Задача C
# -------
# Третий цикл: for по списку из трёх целых чисел, например [7, 8, 9]. Внутри выведи
# не само число, а число, умноженное на 10: print(переменная * 10)
# Ожидание: 70, 80, 90 — так видно: взяли ТВОИ 7 8 9 из списка, range(3) так не сделает.
#
# =============================================================================
# Запуск:  python session3/lesson3_for_range_vs_list.py
