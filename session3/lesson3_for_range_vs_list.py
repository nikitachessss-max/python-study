# =============================================================================
# ОДНА ФРАЗА РАЗНИЦЫ
# =============================================================================
#
#   for x in range(n)     →  x каждый раз ЦЕЛОЕ ЧИСЛО: 0, 1, 2, ... (счётчик шагов)
#
#   for x in список       →  x каждый раз ОЧЕРЕДНОЙ ЭЛЕМЕНТ списка (строка, число, ...)
#
# Слово for одно и то же; меняется только то, что стоит после in — от этого меняется,
# что лежит в переменной x на каждом круге.
#
# Почему задания ниже похожи (везде for и print)?
# Каркас цикла часто один и тот же — «повтори действие несколько раз». Отличается не
# форма for, а ОТКУДА берётся значение в переменной: range даёт 0,1,2…, список даёт
# ТВОИ строки или ТВОИ числа. Три задачи — три разных «источника», чтобы глазами
# сравнить вывод. В B и C тело чуть разное, чтобы на экране было не «три раза одно и то же».
#
# =============================================================================
# Мини-примеры (можно скопировать вниз и запустить по очереди — не в одном блоке,
# а сначала один фрагмент, потом другой, чтобы увидеть разницу)
# =============================================================================
#
# Только range:
#   for i in range(3):
#       print(i)
#   На экране: 0, 1, 2 — три строки, только цифры.
#
# Только список:
#   for word in ["кот", "пёс", "ёж"]:
#       print(word)
#   На экране: три слова, цифр нет (если сам в список числа не положил).
#
# =============================================================================
# Типичная ошибка
# =============================================================================
#
# Список чисел [0, 1, 2] при переборе даёт те же значения, что range(3), и кажется,
# что «всё одно и то же». Но [10, 20, 30] даст 10, 20, 30 — а range(3) никогда так
# не сделает. range задаёт только «сколько кругов» и «от какого числа считать»,
# а не твои свои значения.
#
# =============================================================================
# ЗАДАЧИ (сделай все три в коде ниже, под комментарием КОД)
