# -*- coding: utf-8 -*-
#
# Сессия 3 — чек с позициями, когда ЗАРАНЕЕ НЕИЗВЕСТНО, сколько их будет
#
# Сравни с challenge_sessions_1_3.py: там сначала спрашивают n, потом цикл for на n раз.
# Здесь — цикл «пока кассир не скажет стоп»: сколько раз успеет ввести товары, столько
# и будет позиций.
#
# Запуск:  python session3/lesson3_receipt_unknown_count.py
#
# =============================================================================
# ЗАДАЧА (суть та же, что у челленджа 1–3, но без числа n в начале)
# =============================================================================
#
# 1) Имя кассира и приветствие (как в челлендже).
# 2) Пустой список lines и total = 0.0.
# 3) Цикл while True:
#    - Спроси название товара.
#    - Если название после .strip(), в нижнем регистре, равно "стоп" — ВЫЙДИ (break),
#      не спрашивай цену и не добавляй строку (это не товар, а команда «хватит»).
#    - Иначе спроси цену (float), прибавь к total, собери строку чека с НОМЕРОМ.
#      Номер позиции = сколько строк уже в списке + 1  (или отдельный счётчик +1).
#    - append строки в lines.
# 4) Если в чеке 0 позиций — выведи короткое сообщение и можно завершить или всё равно
#    дойти до while «готово» — на твой выбор; в эталоне ниже — если пусто, печатаем
#    фразу и не лезем в срез.
# 5) Итог total, len(lines), при len >= 3 срез [:3], for по lines — как в челлендже.
# 6) while до слова «готово» (с .strip().lower()) и прощание.
#
# =============================================================================
# ЧТО ИЗМЕНИЛОСЬ по сравнению с вариантом «сначала n» (простыми словами)
# =============================================================================
#
# Раньше: программа спрашивала «сколько позиций?», получала число n, и for делал ровно
# n кругов. Количество было известно ДО ввода товаров.
#
# Здесь: программа не знает, сколько будет позиций. Она снова и снова спрашивает товар,
# пока человек не введёт слово-стоп вместо названия. Сколько раз ввели товар — столько
# позиций. Вместо for i in range(1, n + 1) используется while True и break.
#
# Номер в строке чека (1, 2, 3…) берём не из i цикла for, а так: перед append уже есть
# len(lines) старых строк, значит новая позиция — номер len(lines) + 1.
#
# =============================================================================
# ЭТАЛОН (можно читать и сравнить с challenge_sessions_1_3.py)
# =============================================================================

name = input("Как тебя зовут? ")
print(f"Смена открыта, {name}! Чек без фиксированного числа позиций: товар — цена, "
      f"когда закончишь, вместо товара напиши: стоп")

lines = []
total = 0.0

while True:
    product = input("Название товара (или стоп, чтобы закончить чек): ").strip()
    if product.lower() == "стоп":
        break
    cost = float(input("Цена: "))
    total = total + cost
    position = len(lines) + 1
    line = f"{position}. {product} — {cost} руб."
    lines.append(line)

if len(lines) == 0:
    print("В чеке ни одной позиции — только стоп.")
else:
    print(f"Итого по чеку: {total} руб.")
    print(f"В чеке позиций: {len(lines)}")
    if len(lines) >= 3:
        print("Первые три строки (срез):")
        print(lines[:3])
    print("--- Чек ---")
    for row in lines:
        print(row)

print('Для завершения печати введите слово: готово')
word = input("Введите слово: ").strip().lower()
while word != "готово":
    print("Неверно. Введите слово: готово")
    word = input("Введите слово: ").strip().lower()

print(f"Печать завершена, {name}. Хорошей смены!")
