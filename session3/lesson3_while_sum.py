# -*- coding: utf-8 -*-
"""
Разминка сессии 3 — while + аккумулятор (сумма чисел)

ЗАЧЕМ ЭТА ЗАДАЧА
----------------
В отчёте за сессию 2 было: while и переменная-сумма (total = 0) давались тяжелее,
чем for. Эта задача маленькая, но целенаправленно тренирует то же: цикл + накопление
числа в переменной.

ЧТО НУЖНО СДЕЛАТЬ (УСЛОВИЕ)
---------------------------
Программа много раз просит ввести целое число (int).

Нужно хранить сумму всех введённых чисел в одной переменной (часто её называют
total или summa — как удобно).

Правило остановки: как только пользователь ввёл число 0 — цикл заканчивается.
Сам ноль в сумму не добавляем (то есть 0 — это сигнал «хватит», а не очередное
слагаемое).

После выхода из цикла один раз выведи итоговую сумму (print или f-строка).

КАК ДОЛЖНО ВЫГЛЯДЕТЬ ПОВЕДЕНИЕ (ПРИМЕРЫ)
----------------------------------------
• Ввод подряд: 5, затем 3, затем 0 → сумма должна стать 8, потом программа
  печатает 8 и завершается.
• Если первым же вводом идёт 0 → сумма 0, ничего не прибавляли, только вывод 0.

НА ЧТО ОПЕРЕТЬСЯ (ТЕМЫ)
------------------------
• int(input(...)) — потому что нужны целые числа, не строки.
• Переменная-сумма до цикла: стартовое значение 0 (пустая сумма до первого числа).
• while: пока введённое число не равно 0 — в теле цикла прибавить его к сумме и
  снова спросить следующее число.
• Логика «сначала спросили / пока не ноль / внутри прибавить и снова спросить»
  должна совпасть, чтобы не пропустить число и не задвоить запрос без нужды.
  (Допустимы разные рабочие схемы — главное, чтобы примеры выше давали тот же
  результат.)

ПОДВОДНЫЕ КАМНИ
---------------
• Не путать print и присваивание: ответ пользователя нужно сохранить в переменную.
• Если забудешь внутри цикла снова вызвать input, условие может никогда не
  измениться — зависание.
• Ноль только завершает работу, не обязательно «плюсовать» его к сумме.

ЗАПУСК
------
Из корня проекта:
  python session3/lesson3_while_sum.py

КОД
---
Пиши код ниже этого блока. Если застрянешь — напиши в чат.
"""
total = 0
number = int(input("введи число"))
while number != 0:
    total = total + number
    number = int(input("введи число"))
print (f"итого {total}")



