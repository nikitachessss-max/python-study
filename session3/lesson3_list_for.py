# Сессия 3 — цикл for по списку (с нуля, если range подзабылся)
#
# =============================================================================
# 1) Что вообще такое for
# =============================================================================
#
# for = «сделай вот этот код несколько раз подряд», пока не закончатся «шаги».
# Слово for пишется в начале, потом имя переменной, потом in, потом откуда брать
# шаги (range или список). Строка заканчивается двоеточием.
# Всё, что с отступом под for, — это тот код, который повторяется.
#
# =============================================================================
# 2) Напоминание: for + range (как у тебя было во 2 сессии)
# =============================================================================
#
#   for i in range(3):
#       print(i)
#
# range(3) даёт три «шага»: по сути числа 0, 1, 2.
# Переменная i на первом круге = 0, печатается 0.
# На втором круге i = 1, печатается 1.
# На третьем круге i = 2, печатается 2.
# Потом шаги кончились — цикл сам остановился.
#
# Буква i — просто имя переменной. Мог бы написать for n in range(3) и print(n).
# Python не требует именно i — это ты сам выбираешь имя «коробки», куда кладётся
# очередное число на каждом круге.
#
# =============================================================================
# 3) for + список — та же идея, но в «коробку» кладётся не число из range,
#    а очередной элемент списка
# =============================================================================
#
# Пример:
#
#   colors = ["красный", "зелёный", "синий"]
#   for c in colors:
#       print(c)
#
# Откуда берётся буква c?
#   Ниоткуда «из Python». Это ИМЯ ПЕРЕМЕННОЙ, которое придумал автор примера.
#   Можно было написать for color in colors: print(color) — смысл тот же.
#   Можно for x in colors: print(x). c — просто короткое имя «текущий цвет».
#
# Что делает этот код, по шагам:
#
#   Шаг A.  colors = [...]  — создали список из трёх строк, он лежит в переменной colors.
#
#   Шаг B.  Python входит в for. Берёт ПЕРВЫЙ элемент списка — строку "красный".
#           Кладёт её в переменную c. (Теперь c == "красный".)
#
#   Шаг C.  Выполняется тело цикла: print(c)  → на экран выводится красный
#
#   Шаг D.  Python снова входит в for. Берёт ВТОРОЙ элемент — "зелёный".
#           Перезаписывает c: теперь c == "зелёный".
#
#   Шаг E.  print(c)  → печатается зелёный
#
#   Шаг F.  Третий элемент "синий" → c == "синий" → print(c) → синий
#
#   Шаг G.  Элементы кончились — цикл закончился, программа идёт дальше (если есть код ниже).
#
# Итог на экране три строки: красный, зелёный, синий.
#
# =============================================================================
# 4) Твоя задача
# =============================================================================
#
# 1) Создай список из четырёх строк.
#
# 2) Напиши for по этому списку (имя переменной в for выбери сам — хоть c, хоть word).
#    Внутри цикла один print этой переменной.
#
# В терминале должно получиться четыре строки — по одной на каждый элемент.
#
# =============================================================================
# Запуск:  python session3/lesson3_list_for.py
# =============================================================================
#
# --- КОД ---
# for не сдвигай вправо относительно списка; только print — с отступом под for.
city = ["москва", "питер", "новосибирск", "казань"]
for c in city:
    print(c)
