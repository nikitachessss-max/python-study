# -*- coding: utf-8 -*-
#
# Сессия 3 — while True и break
#
# Запуск:  python session3/lesson3_while_true_break.py
#
# В этом файле один ЖИВОЙ пример.
# Не через цифры, а через слово "ок".
#
# =============================================================================
# Что мы хотим от программы
# =============================================================================
#
# Программа должна снова и снова спрашивать слово.
# Если ввели НЕ "ок" — написать "не то" и спросить ещё раз.
# Если ввели "ок" — закончить цикл и написать "Наконец-то ок!".
#
# =============================================================================
# Главная идея while True
# =============================================================================
#
# while True:
#     ...
#
# Это значит: "крути цикл без конца".
# Сам по себе он НЕ остановится.
# Поэтому внутри должен быть break.
#
# break = "выйди из цикла прямо сейчас".
#
# =============================================================================
# ЖИВОЙ ПРИМЕР С ПОЯСНЕНИЕМ КАЖДОЙ СТРОКИ
# =============================================================================
#
# Ниже код. Читай его сверху вниз.
#
# print("Вводи слово. Чтобы выйти, напиши: ок")
# while True:
#     word = input("Слово: ").strip().lower()
#     if word == "ок":
#         break
#     print("Не то слово, попробуй ещё")
# print("Наконец-то ок!")
#
# Разбор строк:
#
# 1) print("Вводи слово. Чтобы выйти, напиши: ок")
#    Просто сообщение человеку перед циклом.
#
# 2) while True:
#    Начинаем бесконечный цикл.
#    "Бесконечный" не значит "сломанный".
#    Это значит: цикл будет крутиться, пока внутри не встретим break.
#
# 3) word = input("Слово: ").strip().lower()
#    Здесь спрашиваем слово.
#    Что тут по кускам:
#    - input(...) -> берёт текст с клавиатуры
#    - = -> кладём этот текст в переменную word
#    - .strip() -> убирает пробелы по краям
#    - .lower() -> делает буквы маленькими
#    То есть " ОК " станет "ок".
#
# 4) if word == "ок":
#    Проверяем: то слово ввели или не то.
#
# 5) break
#    Если слово "ок" — сразу выходим из цикла.
#    ВАЖНО: строка print("Не то слово...") ниже уже НЕ выполнится.
#
# 6) print("Не то слово, попробуй ещё")
#    Эта строка работает только если break НЕ сработал.
#    То есть только когда ввели не "ок".
#
# 7) print("Наконец-то ок!")
#    Эта строка стоит уже ПОСЛЕ цикла.
#    До неё программа дойдёт только когда цикл закончится через break.
#
# =============================================================================
# Как это идёт по кругам
# =============================================================================
#
# Допустим, человек ввёл сначала "привет", потом "да", потом "ок".
#
# Первый круг:
# - зашли в while True
# - спросили слово
# - word = "привет"
# - if word == "ок" -> нет
# - break НЕ сработал
# - печатаем "Не то слово..."
# - идём на новый круг
#
# Второй круг:
# - снова спросили слово
# - word = "да"
# - if word == "ок" -> нет
# - снова "Не то слово..."
# - идём на новый круг
#
# Третий круг:
# - снова спросили слово
# - word = "ок"
# - if word == "ок" -> да
# - break
# - цикл закончился
# - печатается "Наконец-то ок!"
#
# =============================================================================
# Чем это отличается от while word != "ок"
# =============================================================================
#
# Вариант 1:
#
#   word = input(...)
#   while word != "ок":
#       print("не то")
#       word = input(...)
#
# Тут input написан ДВА раза.
#
# Вариант 2:
#
#   while True:
#       word = input(...)
#       if word == "ок":
#           break
#       print("не то")
#
# Тут input написан ОДИН раз.
# Поэтому многим этот вариант удобнее.
#
# =============================================================================
# ТВОЯ ЗАДАЧА
# =============================================================================
#
# Напиши НИЖЕ такой же код, но с другим словом.
#
# Вместо "ок" сделай слово "стоп".
# Логика та же:
# - while True
# - input
# - if word == "стоп": break
# - иначе print("не то")
# - после цикла print("Остановились")
#
# =============================================================================
# ДЕМО — можно запустить и увидеть живьём
# =============================================================================

print("Вводи слово. Чтобы выйти, напиши: ок")
while True:
    word = input("Слово: ").strip().lower()
    if word == "ок":
        break
    print("Не то слово, попробуй ещё")
print("Наконец-то ок!")

