# Сессия 3 — список: длина len() и последний элемент
#
# --- ЗАЧЕМ ---
#
# Список может быть коротким или длинным. Число элементов даёт встроенная функция
# len(имя_списка) — целое число: 0, 1, 2, 3, ...
#
# Последний элемент всегда имеет индекс на единицу меньше длины:
#   если элементов 5, индексы 0 1 2 3 4 — последний это 4, а len было 5, значит
#   индекс последнего = len - 1
#
# (Индексы «с конца» вроде [-1] существуют в Python — разберём отдельно позже,
# сейчас закрепляем связь именно с len.)
#
# --- НОВОЕ ---
#
#   len(список)          — сколько элементов
#   список[len(список) - 1]   — последний элемент (если список не пустой)
#
# Пример (образец):
#   nums = [10, 20, 30]
#   n = len(nums)       # будет 3
#   last = nums[n - 1]  # то же, что nums[2], значение 30
#
# --- ЗАДАЧА ---
#
# 1) Создай список из четырёх строк (любой смысл — города, игры, что угодно).
#
# 2) Сохрани длину списка в переменную (через len).
#
# 3) Выведи длину (один print или f-строка — как удобнее).
#
# 4) Выведи последний элемент списка, обращаясь по индексу через длину:
#    индекс = длина минус 1. Не хардкодь число 3 — опирайся на переменную с len,
#    чтобы код оставался верным, если потом поменяешь число элементов в списке.
#
# Проверка: в выводе сначала число 4, потом строка — ровно тот элемент, который
# у тебя последний в списке.
#
# --- ЗАПУСК ---
#
#   python session3/lesson3_list_len.py
#
# --- КОД ---

word = ["футбол","хоккей", "баскетбол", "волейбол"]
long = len(word)
print(long)
last = word[long - 1]
print(last)
